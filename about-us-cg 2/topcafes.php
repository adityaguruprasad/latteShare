    <h1>Top Cafes </h1>
    Los Angeles’s coffee scene has come a long way since the days of sugary syrups and poorly foamed milk. The number of local shops doing it right has grown by leaps and bounds in the past few years, making it possible to find a great latte, cappuccino, or brewed coffee in nearly every corner of the city. Here now are the 17 essential coffee bars in Los Angeles, presented from west to east (not in any particular order).


</div>
<h3>1. Interstellar</h3>
<img src="interstellar.png" alt="coffee1" class="size"><br><br>
<div id="more">
    Daniel and Angie Kim opened this well-placed Santa Monica cafe during the pandemic, enduring under difficult circumstances to serve some of the Westside’s best coffee. One of the few LA shops to serve Arkansas’s celebrated Onyx Coffee, Interstellar has an all-day menu of sandwiches, pastries, and innovative coffee drinks.
</div>

<h3>2. House Roots Cafe </h3>
<img src="houserootscafe.png" alt="coffee1" class="size"><br><br>
<div id="more">
    From iced chai with added espresso shots to lavender lattes to straight drip coffee, House Roots manages to do it all well — and with a smile. This daily caffeine driver in Granada Hills also offers a robust collection of sweet and savory pastries to really get the morning going right.
</div>

<h3> 3. goodboybob coffee </h3>
<img src="goodboybob%20coffee.png" alt="coffee1" class="size"><br><br>
<div id="more">
    Located more inland amid Santa Monica’s production offices, hidden away Goodboybob coffee is one of the few shops to have a selection of Cup of Excellence beans, though they’re at a fairly hefty price point. Still, the standard coffee roasts and fantastic food menu are worth venturing to this location, which sports ample outdoor seating. Goodboybob also opened a Culver City spot inside Citizens Public Market food hall.


